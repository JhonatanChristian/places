CREATE TABLE places (
  id bigint NOT NULL AUTO_INCREMENT,
  author bigint NOT NULL,
  place_date datetime,
  place_update datetime,
  place_name text,
  place_content longtext,
  place_street text,
  plane_number varchar(20),
  place_district varchar(200),
  place_city varchar(200),
  place_state varchar(200),
  place_zip_code varchar(20),
  place_address_complement text,
  latitude decimal(10, 8),
  longitude decimal(11, 8),
  comment_status varchar(20),
  place_status varchar(20),
  place_slug varchar(200),
  primary key(id)
);

